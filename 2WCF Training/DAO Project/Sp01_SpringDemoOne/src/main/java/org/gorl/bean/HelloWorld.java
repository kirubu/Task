package org.gorl.bean;

public class HelloWorld 
{
	private String msg;

	public HelloWorld(String msg) 
	{
		this.msg = msg;
	}

	public HelloWorld() 
	{
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}
	
	public void printMsg()
	{
		System.out.println(msg);
	}
	
}
